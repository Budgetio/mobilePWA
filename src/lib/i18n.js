export const LANGUAGES = [
  { value: 'cs', key: 'lang.cs' },
  { value: 'en', key: 'lang.en' },
]

function interpolate(str, vars) {
  if (!vars) return str
  return str.replace(/\{(\w+)\}/g, (_, k) => (vars[k] != null ? vars[k] : `{${k}}`))
}

export function makeT(lang) {
  const table = DICT[lang] || DICT.cs
  return (key, vars) => interpolate(table[key] ?? DICT.cs[key] ?? key, vars)
}

// Počet transakcí se správným tvarem slova.
export function transactionCount(n, lang) {
  if (lang === 'en') return `${n} ${n === 1 ? 'transaction' : 'transactions'}`
  const word = n === 1 ? 'transakce' : n >= 2 && n <= 4 ? 'transakce' : 'transakcí'
  return `${n} ${word}`
}
